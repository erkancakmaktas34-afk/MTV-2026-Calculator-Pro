function getEVRate_2026(kwKey, ageGroup, matrahIndex = 0) {
    if (typeof EV_TAX_TABLE_2026 === 'undefined') return 0;

    // Direct lookup using UI keys (which now match the table keys)
    const kwData = EV_TAX_TABLE_2026[kwKey];
    if (!kwData) return 0;

    // Standardize Age Key
    // Table uses "1-3", "4-6" etc. so we use ageGroup directly
    const ageKey = ageGroup;

    // Check availability
    if (!kwData[ageKey]) return 0;

    const tiers = kwData[ageKey];
    let idx = parseInt(matrahIndex) || 0;

    // CRITICAL: 2019 and older (age 7-11+) always use Tier 0
    if (['7-11', '12-15', '16+'].includes(ageKey)) {
        idx = 0;
    }

    // Safety cap
    if (idx >= tiers.length) idx = tiers.length - 1;

    return tiers[idx];
}

