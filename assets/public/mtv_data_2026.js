function findCCRangeData(ccKey) {
    return MTV_DATA_2026.post_2018_rates.find(item => item.cc_key === ccKey);
}

function findBestMatrixYear(matrix, requestedYear) {
    if (!matrix || Object.keys(matrix).length === 0) return "2026";
    const year = parseInt(requestedYear);
    if (matrix[year]) return String(year);

    const availableYears = Object.keys(matrix).map(Number).sort((a, b) => a - b);
    if (year < availableYears[0]) return String(availableYears[0]);
    if (year > availableYears[availableYears.length - 1]) return String(availableYears[availableYears.length - 1]);

    // Find closest year that doesn't exceed requestedYear
    for (let i = availableYears.length - 1; i >= 0; i--) {
        if (availableYears[i] <= year) return String(availableYears[i]);
    }
    return String(availableYears[0]);
}

