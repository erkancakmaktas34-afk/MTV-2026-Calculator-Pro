function calculateMTV_2026_New(ccKey, tierIndex, ageGroup) {
    // Only used for ICE now
    const ccData = findCCRangeData(ccKey);
    if (!ccData) {
        return 0;
    }

    const ageKey = 'y' + ageGroup.replace('-', '_').replace('+', '_plus');

    let index = parseInt(tierIndex);
    // CRITICAL: 7+ Years vehicles always use Tier 0 (Post-2018 rule)
    if (['y7_11', 'y12_15', 'y16_plus'].includes(ageKey)) {
        index = 0;
    }

    if (isNaN(index)) index = 0;
    if (index >= ccData.tiers.length) index = ccData.tiers.length - 1;

    const tier = ccData.tiers[index];
    let val = tier ? tier[ageKey] : 0;
    return val;
}

