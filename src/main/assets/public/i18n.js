// --- Language Switching ---
function setLanguage(lang) {
    if (!TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    document.documentElement.setAttribute('lang', lang);

    // Update flag to use text instead of emoji so it can be colored via CSS
    const flagEl = document.getElementById('lang-flag');
    if (flagEl) flagEl.innerText = lang.toUpperCase();

    // Re-apply static translations
    applyStaticTranslations();

    // Re-render current view
    if (typeof navigateTo === 'function' && typeof currentView !== 'undefined') {
        navigateTo(currentView);
    }
}

function applyStaticTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (val) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        const val = t(key);
        if (val) el.innerHTML = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const val = t(key);
        if (val) el.setAttribute('title', val);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        const val = t(key);
        if (val) el.setAttribute('aria-label', val);
    });
}

function openLanguagePicker() {
    const opts = Object.entries(LANGS).map(([code, info]) => ({
        value: code,
        label: `${info.flag} ${info.name}`
    }));
    openCustomPicker(
        currentLang === 'tr' ? 'Dil Seçin' : 'Select Language',
        opts, currentLang,
        (val) => setLanguage(val)
    );
}
